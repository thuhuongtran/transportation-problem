package com.vimensa.fashivi.model;

public class Distance {
    private String id;
    private double distance;

    public Distance() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public double getDistance() {
        return distance;
    }

    public void setDistance(double distance) {
        this.distance = distance;
    }
}
